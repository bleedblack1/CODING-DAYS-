public class bitmanupulation {
    
    public static void main(String[] args) {
        //and operator
        System.out.println((5&6));
        //or operator
        System.out.println((5|6));
        // xor operator
        System.out.println((5^6));
         // one's compliment
         System.out.println((~5));
        // right shift
        System.out.println((5>>1));
        // left shift
        System.out.println((5<<1));


    }
    
}
